export function filterRoutines(filters: {
  goal?: string | null
  sport?: string | null
  bodyRegion?: string | null
  level?: string | null
  duration?: number | null
}): Routine[] {
  return routines.filter((r) => {
    if (filters.goal && !r.goals.includes(filters.goal as any)) return false
    if (filters.sport && !r.sports.includes(filters.sport as any)) return false
    if (filters.bodyRegion && !r.bodyRegions.includes(filters.bodyRegion as any)) return false
    if (filters.level && r.level !== filters.level) return false
    if (filters.duration && r.durationMinutes !== filters.duration) return false
    return true
  })
}

export function getRoutineById(id: string): Routine | undefined {
  return routines.find((r) => r.id === id)
}
